
/**
 * SongLyrics assigns string literals (song lyrics) to string objects, which are
 * then printed to the console! The lyrics used in this song are from Daft Punk's
 * "Fragments of Time" from the album Random Access Memories. 
 * 
 * @author Courtney Thurston
 * @version 9/26/2013
 */
public class SongLyrics
{
    public static void main(String [ ] args)
    {
        String firstLineFirstVerse = "Driving this road down to paradise";
        String secondLineFirstVerse = "Letting the sunlight into my eyes";
        String thirdLineFirstVerse = "Our only plan is to improvise";
        String chorusFirstLine = "     I'll just keep playing back";
        String chorusSecondLine = "     These fragments of time";
        String chorusThirdLine = "     Everywhere I go, these moments will shine";
        String firstLineSecondVerse = "Familiar faces I've never seen";
        String secondLineSecondVerse = "Living the gold and the silver dream";
        String thirdLineSecondVerse = "Making me feel like I'm 17";
    
        System.out.println(firstLineFirstVerse);
        System.out.println(secondLineFirstVerse);
        System.out.println(thirdLineFirstVerse);
        System.out.println(chorusFirstLine);
        System.out.println(chorusSecondLine);
        System.out.println(chorusThirdLine);
        System.out.println();
        System.out.println(firstLineSecondVerse);
        System.out.println(secondLineSecondVerse);
        System.out.println(thirdLineSecondVerse);
        System.out.println(chorusFirstLine);
        System.out.println(chorusSecondLine);
        System.out.println(chorusThirdLine);        
    }
}
